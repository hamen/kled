class Colors {
    companion object {
        public val OFF = "off" to 0x00
        public val BLUE = "blue" to 0x01
        public val RED = "red" to 0x02
        public val GREEN = "green" to 0x03
        public val LTBLUE = "liteblue" to 0x04
        public val PURPLE = "purple" to 0x05
        public val YELLOW = "yellow" to 0x06
        public val WHITE = "white" to 0x07

        public val colors = hashMapOf(OFF, BLUE, RED, GREEN, LTBLUE, PURPLE, YELLOW, WHITE)
    }
}